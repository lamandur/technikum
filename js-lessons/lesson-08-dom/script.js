/*
ТЕОРИЯ

ЧТО ТАКОЕ DOM?

DOM (Document Object Model) — это объектное представление HTML-документа.
Страница представляется как дерево элементов, с которыми можно взаимодействовать из JavaScript.

ПОЛУЧЕНИЕ ЭЛЕМЕНТОВ:

1. По ID:
   document.getElementById('id')

2. По селектору CSS:
   document.querySelector('.class')      // первый элемент
   document.querySelectorAll('.class')   // все элементы

3. По тегу:
   document.getElementsByTagName('p')

4. По классу:
   document.getElementsByClassName('class')

5. По имени атрибута:
   document.getElementsByName('name')

ИЗМЕНЕНИЕ КОНТЕНТА:

1. Текст:
   element.textContent = "Новый текст"

2. HTML:
   element.innerHTML = "<strong>Жирный текст</strong>"

ИЗМЕНЕНИЕ СТИЛЕЙ:

1. Прямое изменение:
   element.style.color = "red";
   element.style.backgroundColor = "yellow";
   element.style.fontSize = "20px";

2. Добавление/удаление классов:
   element.classList.add('class-name');
   element.classList.remove('class-name');
   element.classList.toggle('class-name');  // переключает

СОЗДАНИЕ И ДОБАВЛЕНИЕ ЭЛЕМЕНТОВ:

1. Создать элемент:
   let newElement = document.createElement('div');

2. Установить контент:
   newElement.textContent = "Новый элемент";

3. Добавить на страницу:
   parentElement.appendChild(newElement);

УДАЛЕНИЕ ЭЛЕМЕНТОВ:

1. Удалить элемент:
   element.remove();

2. Удалить дочерний элемент:
   parentElement.removeChild(childElement);

ПОЛЕЗНЫЕ СВОЙСТВА:

- element.tagName      — имя тега (например, "DIV")
- element.className    — класс элемента
- element.id           — id элемента
- element.children     — все дочерние элементы
- element.parentElement — родительский элемент
*/

