/*
ТЕОРИЯ

ЧТО ТАКОЕ ОБЪЕКТ?

Объект — это коллекция пар "ключ-значение" (свойств).

СОЗДАНИЕ ОБЪЕКТА:

// Литеральная нотация (самый распространённый способ):
let person = {
    name: "Иван",
    age: 25,
    isStudent: true
};

// Через конструктор (реже используется):
let person = new Object();
person.name = "Иван";
person.age = 25;

ДОСТУП К СВОЙСТВАМ:

// Через точку:
person.name      → "Иван"
person.age       → 25

// Через квадратные скобки:
person["name"]   → "Иван"

ИЗМЕНЕНИЕ СВОЙСТВ:

person.age = 26;              // изменить существующее
person.city = "Москва";       // добавить новое
delete person.isStudent;      // удалить свойство

МЕТОДЫ ОБЪЕКТА:

Метод — это функция, которая является свойством объекта.

let person = {
    name: "Иван",
    age: 25,
    greet: function() {
        console.log("Привет, меня зовут " + this.name);
    }
};

person.greet();  // "Привет, меня зовут Иван"

КЛЮЧЕВОЕ СЛОВО THIS:

this внутри метода ссылается на текущий объект.

let person = {
    name: "Иван",
    getName: function() {
        return this.name;  // this = person
    }
};

person.getName();  // "Иван"

ВЛОЖЕННЫЕ ОБЪЕКТЫ:

Объекты могут содержать другие объекты.

let student = {
    name: "Иван",
    address: {
        city: "Москва",
        street: "Тверская",
        house: 10
    }
};

student.address.city  → "Москва"

МАССИВЫ ОБЪЕКТОВ:

let students = [
    { name: "Иван", age: 20 },
    { name: "Мария", age: 22 },
    { name: "Алексей", age: 19 }
];

students[0].name  → "Иван"
*/

