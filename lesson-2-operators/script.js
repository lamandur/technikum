/*
ТЕОРИЯ

АРИФМЕТИЧЕСКИЕ ОПЕРАТОРЫ:
+   — сложение          5 + 3   → 8
-   — вычитание         5 - 3   → 2
*   — умножение         5 * 3   → 15
/   — деление           6 / 3   → 2
%   — остаток от деления  7 % 3 → 1
**  — возведение в степень  2**3 → 8

ОПЕРАТОРЫ СРАВНЕНИЯ:
==   — равно (с приведением типов)     5 == "5"   → true
===  — строго равно (тип + значение)   5 === "5"  → false
!=   — не равно (с приведением)        5 != "6"   → true
!==  — строго не равно                 5 !== "5"  → true
>    — больше                          5 > 3      → true
<    — меньше                          5 < 3      → false
>=   — больше или равно                5 >= 5     → true
<=   — меньше или равно                5 <= 3     → false

ЛОГИЧЕСКИЕ ОПЕРАТОРЫ:
&&  — И (оба условия должны быть true)
    (5 > 3) && (2 < 4)  → true
    (5 > 3) && (2 > 4)  → false

||  — ИЛИ (хотя бы одно условие true)
    (5 > 3) || (2 > 4)  → true
    (5 < 3) || (2 > 4)  → false

!   — НЕ (инверсия)
    !(5 > 3)  → false
    !(5 < 3)  → true

ОПЕРАТОРЫ ПРИСВАИВАНИЯ:
=    — присвоить           x = 5
+=   — прибавить и присвоить  x += 3  → x = x + 3
-=   — вычесть и присвоить    x -= 3  → x = x - 3
*=   — умножить и присвоить   x *= 3  → x = x * 3
/=   — разделить и присвоить  x /= 3  → x = x / 3

ИНКРЕМЕНТ И ДЕКРЕМЕНТ:
++  — увеличить на 1    x++  или  ++x
--  — уменьшить на 1    x--  или  --x

ПРИОРИТЕТ ОПЕРАТОРОВ (от высшего к низшему):
1. ()       — скобки
2. **       — возведение в степень
3. * / %    — умножение, деление, остаток
4. + -      — сложение, вычитание
5. < <= > >= — сравнения
6. == != === !== — равенства
7. &&       — логическое И
8. ||       — логическое ИЛИ
*/

