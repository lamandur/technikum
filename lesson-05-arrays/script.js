/*
ТЕОРИЯ

ЧТО ТАКОЕ МАССИВ?

Массив — это упорядоченный список значений.

Создание массива:
    let arr = [1, 2, 3, 4, 5];
    let names = ["Иван", "Мария", "Алексей"];

ДОСТУП К ЭЛЕМЕНТАМ:

Элементы массива нумеруются с 0 (нулевой индекс).

    let arr = [10, 20, 30];
    arr[0]  → 10  (первый элемент)
    arr[1]  → 20  (второй элемент)
    arr[2]  → 30  (третий элемент)

ИЗМЕНЕНИЕ ЭЛЕМЕНТА:

    arr[1] = 25;  // второй элемент стал 25

ДЛИНА МАССИВА:

    arr.length  → 3

ОСНОВНЫЕ МЕТОДЫ:

Добавление/удаление:
    arr.push(элемент)      — добавить в конец
    arr.pop()              — удалить из конца
    arr.unshift(элемент)   — добавить в начало
    arr.shift()            — удалить из начала

Перебор массива:
    arr.forEach(функция)   — выполнить для каждого элемента

Преобразование массива:
    arr.map(функция)       — создать новый массив, применив функцию к каждому элементу
    arr.filter(функция)    — создать новый массив из элементов, прошедших проверку
    arr.find(функция)      — найти первый элемент, прошедший проверку

ПОИСК И ПРОВЕРКА:
    arr.includes(значение) — содержит ли массив значение (true/false)
    arr.indexOf(значение)  — индекс элемента (-1 если не найден)
*/

